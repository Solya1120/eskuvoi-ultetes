    private static List<Resztvevo> loadResztvevok(){
        List<Resztvevo> vendegek = new ArrayList<>();
        try{
        
            String filename = "Resztvevo.xml";
            File file = new File(filename);
            DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
            DocumentBuilder builder = factory.newDocumentBuilder();
            Document xml = builder.parse(file);
            xml.normalize();
            
            NodeList vendegCsomopontok = xml.getElementsByTagName("Resztvevo");
            for(Integer i = 0; i < vendegCsomopontok.getLength(); i++){
                Node vendegCsomopont = vendegCsomopontok.item(i);
                Element vendegAdatai = (Element)vendegCsomopont;
                
                String statuszStr = vendegAdatai.getElementsByTagName("statusz").item(0).getTextContent();
                String szekSzamStr = vendegAdatai.getElementsByTagName("szekSzam").item(0).getTextContent();
                String telefonszam = vendegAdatai.getElementsByTagName("telefonszam").item(0).getTextContent();
                String resztVeszStr = vendegAdatai.getElementsByTagName("resztVesz").item(0).getTextContent();
                String nev = vendegAdatai.getElementsByTagName("nev").item(0).getTextContent();
                String email = vendegAdatai.getElementsByTagName("email").item(0).getTextContent();
                
                Integer szekSzam = Integer.parseInt(szekSzamStr);
                Boolean resztVesz = Boolean.parseBoolean(resztVeszStr);
                STATUSZ statusz = STATUSZ.valueOf(statuszStr);
                                
                Resztvevo r = new Resztvevo(statusz, nev, email, telefonszam, resztVesz, szekSzam);
                vendegek.add(r);
            }
            
        
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Nem sikerült betölteni a vendégek adatait! (" + ex.toString() + ")");
        }
        return vendegek;
    }
    
