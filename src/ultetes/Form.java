    private void keszBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_keszBtnMouseClicked
        String nev= this.nevField.getText();
        if(nev.length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Kérem, írja be a nevét!");
        }
        else{
            String email = this.emailField.getText();
            String telefonszam = this.telefonszamField.getText();
            Boolean resztVesz = this.resztVeszRbtn.isSelected();
            STATUSZ statusz = STATUSZ.VOLEGENYKOZELI;
            String statuszStr = this.vendegList.getSelectedItem().toString();
            
            
            for(STATUSZ f : STATUSZ.values()){
                if(f.name().equals(statuszStr)){
                    statusz = f;
                    break;
                }
            }
            
            Resztvevo vendeg = new Resztvevo(statusz, nev, email, telefonszam, resztVesz, szekSzam);
            
            Funkciok<Resztvevo> v = new Funkciok<>();
            v.mentes(vendeg);
            this.vendegek.add(vendeg);
            
            szekek.get(szekSzam).szekFoglalas();
            osszesSzekMentese();
            
            szekSzam = -1;
            
            this.nevField.setText("");
            this.emailField.setText("");
            this.telefonszamField.setText("");
            this.resztVeszRbtn.setSelected(Boolean.TRUE);
            this.vendegList.setSelectedIndex(0);
            this.VendegPanel.setVisible(Boolean.FALSE);
            
            szekSzinezes();
            eddigiVendegekListFrissit();
            
            FoglalasPanel.setVisible(Boolean.FALSE);
            AdatokPanel.setVisible(Boolean.FALSE);
            VendegVagyokBtn.setVisible(Boolean.TRUE);
            SzolgaltatoVagyokBtn.setVisible(Boolean.TRUE);
        }
    }//GEN-LAST:event_keszBtnMouseClicked

    private void keszBtn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_keszBtn1MouseClicked
       String nev= this.nevField1.getText();
        if(nev.length() < 1 ||  szekSzam < 0){
            JOptionPane.showMessageDialog(rootPane, "Hibás kitöltés, a név mező kitöltése és szék foglalás kötelező!");
        }
        else{
            String email = this.emailField1.getText();
            String telefonszam = this.telefonszamField1.getText();
            String szolgaltatas = this.szolgaltatasField1.getText();
            Boolean resztVesz = this.resztVeszRbtn1.isSelected();
            Integer ar = Integer.parseInt(this.arField1.getText());
            
            
            Szolgaltato szolgaltato = new Szolgaltato(nev, email, telefonszam, resztVesz, ar, szolgaltatas, szekSzam);
            
            Funkciok<Szolgaltato> sz = new Funkciok<>();
            sz.mentes(szolgaltato);
            this.szolgaltatok.add(szolgaltato);
            
            szekek.get(szekSzam).szekFoglalas();
            osszesSzekMentese();
            
            szekSzam = -1;
            
            this.nevField1.setText("");
            this.emailField1.setText("");
            this.telefonszamField1.setText("");
            this.resztVeszRbtn1.setSelected(Boolean.TRUE);
            this.arField1.setText("");
            this.szolgaltatasField1.setText("");
            this.SzolgaltatoPanel.setVisible(Boolean.FALSE);
            
            szekSzinezes();
            eddigiSzolgaltatokListFrissit();
            
            FoglalasPanel.setVisible(Boolean.FALSE);
            AdatokPanel.setVisible(Boolean.FALSE);
            VendegVagyokBtn.setVisible(Boolean.TRUE);
            SzolgaltatoVagyokBtn.setVisible(Boolean.TRUE);
            
        }
    }//GEN-LAST:event_keszBtn1MouseClicked

